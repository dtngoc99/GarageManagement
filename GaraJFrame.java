                kt1=true;
                jTextField9.setText("Successful");
                card=ca;
                jTextField11.setText(String.valueOf(card.getCardType()));
                jTextField2.setText(String.valueOf(card.getCheckInDay()));
                jTextField3.setText(String.valueOf(card.getCheckInMonth()));
                jTextField4.setText(String.valueOf(card.getCheckInHour()));

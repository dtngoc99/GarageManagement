    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        if(kt==true){//checkin
            try {
                ga.cardList.add(card);
                ga.writeFile();
                jTextField10.setText("Successful");
            } catch (IOException ex) {
                Logger.getLogger(GaraJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{//checkout
            for(Card c: ga.cardList){
                if(card.licensePlate.equals(c.licensePlate)){
                    jTextField8.setText(String.valueOf(card.Money()));
                    ga.cardList.remove(c);
                    try {
                        ga.writeFile();
                    } catch (IOException ex) {
                        Logger.getLogger(GaraJFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    break;
                }
            }
        }
        ga.cardList.clear();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3MouseClicked

